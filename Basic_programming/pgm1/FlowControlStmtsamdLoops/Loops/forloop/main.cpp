// for Loop
// Concept

// The for loop repeats code a fixed number of times.


// Syntax
// for(initialization; condition; increment)
// {
//     // code
// }


#include<iostream>
using namespace std;
int main()
{
    for(int i = 1; i <= 5; i++)
    {
        cout << i << endl;
    }
    return 0;
}