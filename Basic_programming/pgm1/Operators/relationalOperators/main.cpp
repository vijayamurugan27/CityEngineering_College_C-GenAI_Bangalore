// Relational operators compare two values.

// Operator	Meaning
// ==	        Equal to
// !=	        Not equal to
// >	        Greater than
// <	        Less than
// >=	        Greater than or equal
// <=	        Less than or equal

#include<iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 20;

    cout << (a == b) << endl;
    cout << (a != b) << endl;
    cout << (a > b) << endl;
    cout << (a < b);

    return 0;
}